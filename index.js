const name = "Andrew Harper";

// Template Literal
console.log(`Hello World! This is ${name}`);

// Object Literal
const me = {
  name:"Drew",
  age: 28,
  weight: 190,
  status: "student",
};

// We are using bracket notation
console.log(me["name"]);

// We are using dot notation
console.log(me.age)
